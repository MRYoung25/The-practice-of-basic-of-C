#include <stdio.h>

int main()
{
    printf("Hello World\n");
    printf("Number: %d\n", -3);
    printf("Unsiged number: %u\n", 23576);
    printf("%o\n", 8);
    printf("%X, %x, Red:%X\n", 13, 12, 0xFFFF0000);
    printf("%.2f\n", 3.14);
    printf("%c\n", 'B');
    printf("%s\n", "Hello World");
    printf("%04d\n", 300);
    return 0;
}
